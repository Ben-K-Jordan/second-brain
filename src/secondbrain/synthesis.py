"""Phase 72 + 73 + 74 + 75: synthesis & active surfacing.

Four separate features that all share the same underlying motion —
look across the brain, find a pattern, surface it to the user. Kept
in one module because they share a lot of plumbing (LLM calls, the
``synthesis_runs`` table, common query helpers).

Phase 72 — Weekly review
  Sunday: assemble the week's activity (meetings, lectures, jobs,
  tasks done, top topics, open loops > 7d) into a single Markdown doc.
  Editable + indexable. Same shape as the daily brief but week-scope
  + LLM-summarised tone.

Phase 73 — Smart projects
  Auto-detect clusters of recent docs that hover around one theme.
  Uses the Phase 52 backlinks graph: pick a seed doc from the last
  N days, walk its links, score the cluster by topical density.
  Surfaces 3-5 candidate "projects" the user can explicitly tag (via
  Phase 81's project tracker).

Phase 74 — Auto-summary for long docs
  Indexer hook: docs >= 5000 characters get a TL;DR + key points
  generated by Claude Haiku. Stored in ``doc_summaries`` so search
  results can show a preview without re-summarising on every render.

Phase 75 — "I noticed X"
  Periodic pattern detection that fires proactively. Two flavours:
    - **Topic spikes**: "you've referenced 'voyage rate limits' in
      4 docs this week — want a synthesis?"
    - **Health drift**: "your sleep is 15% below your 30-day avg
      this week — here's what changed in your schedule."
  Fires at most once per insight per week (deduped by insight key).
"""

