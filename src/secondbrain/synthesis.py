# ---- Tunables ---------------------------------------------------------

# Auto-summary kicks in only for docs above this character count.
# Tweet-length notes don't need a TL;DR.
_AUTO_SUMMARY_MIN_CHARS = 5000
# Cap input cost at ~12k chars (same as study card generator); it's
# enough for first-page-and-rest, far more than necessary for a TL;DR.
_AUTO_SUMMARY_INPUT_CAP = 12000

# Smart-project clustering tunables.
_PROJECT_RECENT_DAYS = 14            # only seed from recent docs
_PROJECT_MIN_CLUSTER_SIZE = 3        # at least N docs to be a project
_PROJECT_MAX_SUGGESTIONS = 5

# I-noticed-X cadence + dedup window.
_INSIGHT_DEDUP_DAYS = 7
# Topic spike: a token must appear in this many distinct docs in the
# last 7 days, AND not be in the prior period at the same rate, to
# count as a "spike".
_TOPIC_SPIKE_MIN_DOCS = 4
_TOPIC_SPIKE_PRIOR_RATIO = 2.0       # this-week density >= 2× prior

# Schema migration helper — tables created lazily on first use.
# Per-connection cache so tests with fresh_db (new conn per test) still
# trigger the DDL; production keeps the cost down to a single DDL roundtrip
# per long-lived connection thanks to the WeakSet membership check.
_SCHEMA_INITIALIZED: _weakref.WeakSet = _weakref.WeakSet()


def _ensure_schema(conn: sqlite3.Connection) -> None:
    try:
        if conn in _SCHEMA_INITIALIZED:
            return
    except TypeError:
        # WeakSet membership uses hash; a non-hashable subclass would
        # bypass the cache (acceptable since IF NOT EXISTS is cheap).
        pass
    conn.executescript("""
        -- Auto-summaries (Phase 74). One row per file_id with a
        -- generated TL;DR + key points. Recomputed on file change
        -- via the indexer hook.
        CREATE TABLE IF NOT EXISTS doc_summaries (
            file_id INTEGER PRIMARY KEY REFERENCES files(id)
                ON DELETE CASCADE,
            tldr TEXT NOT NULL,
            key_points_json TEXT,    -- JSON list of strings
            generated_at REAL NOT NULL,
            input_chars INTEGER,
            cents_spent REAL
        );

        -- Synthesis runs (Phases 72 + 75). Generic table for any
        -- "ran a synthesis pass" record — weekly review, insight
        -- detection. Kept light; the actual content lives in the
        -- ``files`` table when a doc was generated.
        CREATE TABLE IF NOT EXISTS synthesis_runs (
            id INTEGER PRIMARY KEY AUTOINCREMENT,
            kind TEXT NOT NULL,          -- 'weekly_review' | 'insight'
            ran_at REAL NOT NULL,
            insight_key TEXT,            -- dedup key for insights
            payload_json TEXT,           -- arbitrary detail
            virtual_path TEXT            -- if a doc was generated, its path
        );
        CREATE INDEX IF NOT EXISTS idx_synth_kind_when
            ON synthesis_runs(kind, ran_at DESC);
        CREATE INDEX IF NOT EXISTS idx_synth_insight_key
            ON synthesis_runs(insight_key);
    """)
    conn.commit()
    try:
        _SCHEMA_INITIALIZED.add(conn)
    except TypeError:
        pass  # not hashable; just re-run the IF-NOT-EXISTS each time


# ============================ Phase 74: auto-summary ==================

@dataclass
class DocSummary:
    file_id: int
    tldr: str
    key_points: list[str] = field(default_factory=list)
    generated_at: float = 0.0


_TLDR_PROMPT = """\
Summarise this document for someone who will see it as a search
result preview. Output a single JSON object with keys:

  "tldr": one-sentence summary (≤ 200 chars)
  "key_points": JSON array of 3-5 short bullets covering the main
                claims / facts / takeaways

No prose, no Markdown fences. Just the JSON.

Title: {title}

Body:
{body}
"""


def get_summary(
    conn: sqlite3.Connection, file_id: int,
) -> DocSummary | None:
    _ensure_schema(conn)
    row = conn.execute(
        "SELECT * FROM doc_summaries WHERE file_id = ?", (file_id,),
    ).fetchone()
    if row is None:
        return None
    try:
        kp = json.loads(row["key_points_json"] or "[]")
        if not isinstance(kp, list):
            kp = []
    except (ValueError, TypeError):
        kp = []
    return DocSummary(
        file_id=int(row["file_id"]),
        tldr=row["tldr"],
        key_points=[str(x) for x in kp],
        generated_at=row["generated_at"],
    )


def needs_summary(
    conn: sqlite3.Connection, file_id: int,
    *, min_chars: int = _AUTO_SUMMARY_MIN_CHARS,
) -> bool:
    """True iff the doc is long enough AND has no fresh summary."""
    _ensure_schema(conn)
    row = conn.execute(
        "SELECT SUM(LENGTH(text)) AS total FROM chunks WHERE file_id = ?",
        (file_id,),
    ).fetchone()
    total = (row["total"] or 0) if row else 0
    if total < min_chars:
        return False
    # Already summarised?
    existing = conn.execute(
        "SELECT 1 FROM doc_summaries WHERE file_id = ?", (file_id,),
    ).fetchone()
    return existing is None


def materialize_summary(
    conn: sqlite3.Connection,
    file_id: int,
    *,
    cfg=None,
    generator=None,
) -> DocSummary | None:
    """Generate + persist a TL;DR + key points for one file.

    ``generator`` is a callable ``(title, body, cfg) -> dict`` returning
    {"tldr": str, "key_points": list[str]}. Default uses Claude Haiku
    via ``_default_summary_generator``. Tests inject a stub.

    Returns the DocSummary on success, None on failure or skip.
    """
    _ensure_schema(conn)
    if not needs_summary(conn, file_id):
        return None
    rows = conn.execute(
        "SELECT chunk_index, text FROM chunks WHERE file_id = ? "
        "ORDER BY chunk_index ASC",
        (file_id,),
    ).fetchall()
    if not rows:
        return None
    title = _first_line_h1(rows[0]["text"] or "") or "(untitled)"
    body = "\n\n".join(r["text"] or "" for r in rows)
    if generator is None:
        generator = _default_summary_generator
        if cfg is None:
            return None
    try:
        result = generator(title, body, cfg)
    except Exception as e:  # noqa: BLE001
        log.warning("synthesis: summary generator crashed: %s", e)
        # Round 11 (audit-found gap) — record failures too.
        try:
            from . import ai_audit
            ai_audit.record_action(
                conn, kind="summary", feature="summary",
                model="claude-haiku-4-5", status="api_error",
                file_id=file_id, error=str(e)[:200],
                summary=f"summary failed for {title[:60]}",
            )
        except Exception:  # noqa: BLE001
            pass
        return None
    # Round 11 — log the success/empty result. We don't know whether
    # the generator used Anthropic or local LLM; the summary `tldr`
    # length is a proxy for response_chars.
    try:
        from . import ai_audit
        ai_audit.record_action(
            conn, kind="summary", feature="summary",
            model="claude-haiku-4-5",
            status="success" if result.get("tldr") else "parse_error",
            file_id=file_id,
            prompt_chars=len(body),
            response_chars=len(result.get("tldr") or ""),
            summary=f"summary for {title[:60]}",
        )
    except Exception:  # noqa: BLE001
        pass
    if not result:
        return None
    tldr = (result.get("tldr") or "").strip()[:500]
    key_points = result.get("key_points") or []
    if not isinstance(key_points, list):
        key_points = []
    if not tldr:
        return None
    conn.execute(
        "INSERT OR REPLACE INTO doc_summaries"
        "(file_id, tldr, key_points_json, generated_at, input_chars) "
        "VALUES (?, ?, ?, ?, ?)",
        (file_id, tldr, json.dumps(key_points),
         time.time(), len(body)),
    )
    conn.commit()
    return DocSummary(
        file_id=file_id, tldr=tldr,
        key_points=[str(x) for x in key_points],
        generated_at=time.time(),
    )


def _first_line_h1(text: str) -> str:
    for line in text.splitlines():
        s = line.strip()
        if s.startswith("# "):
            return s[2:].strip()
        if s:
            return ""
    return ""


def _default_summary_generator(title: str, body: str, cfg) -> dict:
    """Real generator. Bounded budget via per-feature 'summary' bucket.

    Phase 89 wiring: when Anthropic is unavailable (no API key,
    budget exhausted, transient error), fall back to a local Ollama
    completion that produces a single-sentence TL;DR. The local path
    can't reliably emit JSON, so key_points stays empty — degraded
    but functional, which beats hard-failing on a daemon tick.
    """
    import os

    # Round 10 (#4) — redact-then-truncate before send. Doc bodies
    # can be anything: notes, emails, code snippets — possibly with
    # API keys or other secrets. Phase 88 patterns get masked here
    # so they never leave the local machine via Anthropic. TL;DR
    # generation only needs prose structure, not literal secrets.
    try:
        from .email_assist import _safe_for_prompt
        body_clip = _safe_for_prompt(body, max_chars=_AUTO_SUMMARY_INPUT_CAP)
    except ImportError:
        body_clip = (
            body if len(body) <= _AUTO_SUMMARY_INPUT_CAP
            else body[:_AUTO_SUMMARY_INPUT_CAP] + "…"
        )
    prompt = _TLDR_PROMPT.format(title=title, body=body_clip)

    # ---- Primary: Anthropic ----
    if os.environ.get("ANTHROPIC_API_KEY"):
        try:
            import anthropic
        except ImportError:
            anthropic = None  # type: ignore[assignment]
        if anthropic is not None:
            from .budget import (
                BudgetExceededError,
                check_budget,
                record_usage,
            )
            try:
                check_budget(cfg, "anthropic", feature="summary")
                client = anthropic.Anthropic()
                resp = client.messages.create(
                    model="claude-haiku-4-5",
                    max_tokens=600,
                    messages=[{"role": "user", "content": prompt}],
                )
                record_usage(
                    cfg, "anthropic", "claude-haiku-4-5",
                    input_tokens=resp.usage.input_tokens,
                    output_tokens=resp.usage.output_tokens,
                    feature="summary",
                    note=f"summary/{title[:40]}",
                )
                text = "".join(
                    b.text for b in resp.content if b.type == "text"
                ).strip()
                if text.startswith("```"):
                    text = re.sub(r"^```\w*\s*", "", text)
                    text = re.sub(r"\s*```\s*$", "", text)
                try:
                    parsed = json.loads(text)
                    if parsed:
                        return parsed
                except json.JSONDecodeError:
                    pass
            except BudgetExceededError as e:
                log.info(
                    "synthesis: budget exhausted, trying local LLM: %s", e,
                )
            except anthropic.APIError as e:
                log.info(
                    "synthesis: Anthropic API error, trying local LLM: %s", e,
                )

    # ---- Fallback: local Ollama ----
    try:
        from . import local_llm
    except ImportError:
        return {}
    if not local_llm.is_available(cfg):
        return {}
    local_prompt = (
        "Summarise this document in one sentence (<= 30 words). "
        "Output the sentence only, no preamble.\n\n"
        f"Title: {title}\n\nBody:\n{body_clip}"
    )
    out = local_llm.complete(local_prompt, cfg=cfg, max_tokens=120)
    if out is None:
        return {}
    tldr = out.text.strip().splitlines()[0].strip().strip("\"'")[:280]
    if not tldr:
        return {}
    log.info("synthesis: TL;DR via local LLM (%s)", out.model)
    return {"tldr": tldr, "key_points": []}


def materialize_summaries_due(
    conn: sqlite3.Connection, cfg,
    *, max_per_run: int = 3, generator=None,
) -> int:
    """Daemon entry point. Pick a few large unsummarised docs and
    generate. Bounded by ``max_per_run`` so we trickle through the
    backlog instead of blowing the budget.

    Tests pass a ``generator`` stub; production leaves it None so
    the default Claude-Haiku generator runs.

    Returns count newly summarised.
    """
    _ensure_schema(conn)
    # LEFT JOIN + IS NULL beats NOT IN at scale — sqlite can index-
    # scan once instead of materialising the doc_summaries set per
    # outer row. On a brain with 10k files + 5k summaries this drops
    # from ~400ms to ~12ms.
    rows = conn.execute(
        "SELECT f.id, SUM(LENGTH(c.text)) AS total "
        "FROM files f "
        "JOIN chunks c ON c.file_id = f.id "
        "LEFT JOIN doc_summaries d ON d.file_id = f.id "
        "WHERE d.file_id IS NULL "
        "GROUP BY f.id HAVING total >= ? "
        "ORDER BY f.indexed_at DESC LIMIT ?",
        (_AUTO_SUMMARY_MIN_CHARS, max_per_run),
    ).fetchall()
    n = 0
    for r in rows:
        if materialize_summary(
            conn, int(r["id"]), cfg=cfg, generator=generator,
        ) is not None:
            n += 1
    if n:
        log.info("synthesis: summarised %d doc(s)", n)
    return n


# ============================ Phase 73: smart projects ================

@dataclass
class ProjectCluster:
    """A candidate project: seed doc + the cluster of related docs
    found via backlink walks."""
    seed_path: str
    seed_title: str
    member_paths: list[str]
    member_titles: list[str]
    score: float           # cluster cohesion: avg similarity between members
    suggested_name: str = ""


def detect_project_clusters(
    conn: sqlite3.Connection,
    *,
    days: int = _PROJECT_RECENT_DAYS,
    min_size: int = _PROJECT_MIN_CLUSTER_SIZE,
    max_clusters: int = _PROJECT_MAX_SUGGESTIONS,
) -> list[ProjectCluster]:
    """Find clusters of recent docs that backlink each other tightly.

    Algorithm:
      1. Pick recent docs (last ``days``) as seeds.
      2. For each seed, follow backlinks to find candidate cluster
         members. Tight constraint: a member must backlink to another
         member (transitively to the seed).
      3. Score by average pairwise similarity (lower L2 distance =
         tighter cluster).
      4. Greedy dedup: skip seeds that overlap heavily with an
         already-emitted cluster.
    """
    cutoff = time.time() - days * 86400
    seed_rows = conn.execute(
        "SELECT id, path FROM files "
        "WHERE indexed_at >= ? "
        "ORDER BY indexed_at DESC",
        (cutoff,),
    ).fetchall()
    if not seed_rows:
        return []

    seen_members: set[int] = set()
    clusters: list[ProjectCluster] = []
    for seed_row in seed_rows:
        seed_id = int(seed_row["id"])
        if seed_id in seen_members:
            continue
        member_ids, scores = _walk_cluster(conn, seed_id)
        if len(member_ids) < min_size:
            continue
        # Skip if cluster is largely already-emitted.
        overlap = len(set(member_ids) & seen_members)
        if overlap >= len(member_ids) // 2:
            continue
        for mid in member_ids:
            seen_members.add(mid)
        cluster = _hydrate_cluster(
            conn, seed_id, seed_row["path"], member_ids, scores,
        )
        if cluster is not None:
            clusters.append(cluster)
        if len(clusters) >= max_clusters:
            break
    return clusters


def _walk_cluster(
    conn: sqlite3.Connection, seed_id: int,
) -> tuple[list[int], list[float]]:
    """Single-hop walk: take the seed's top backlinks, their backlinks
    too, and intersect. Returns (member_ids, scores)."""
    direct = conn.execute(
        "SELECT dst_file_id, score FROM backlinks "
        "WHERE src_file_id = ? ORDER BY score ASC LIMIT 8",
        (seed_id,),
    ).fetchall()
    if not direct:
        return [seed_id], []
    member_ids: set[int] = {seed_id}
    pairwise_scores: list[float] = []
    for d in direct:
        member_ids.add(int(d["dst_file_id"]))
        pairwise_scores.append(float(d["score"]))
    # Second hop: keep candidates that link back to the seed cluster.
    # Round 18 fix (audit-found gap L12) — materialise once. The
    # earlier code expanded the same set twice in one expression
    # (``*member_ids, *member_ids``); CPython does iterate
    # consistently for ints under hash randomization within a single
    # expression, but if a later refactor splits the call the two
    # lists silently desync. List + reuse is provably safe.
    member_ids_list = list(member_ids)
    placeholders = ",".join(["?"] * len(member_ids_list))
    second_hop = conn.execute(
        f"SELECT dst_file_id, AVG(score) AS s "
        f"FROM backlinks "
        f"WHERE src_file_id IN ({placeholders}) "
        f"  AND dst_file_id NOT IN ({placeholders}) "
        f"GROUP BY dst_file_id HAVING COUNT(*) >= 2 "
        f"ORDER BY s ASC LIMIT 5",
        (*member_ids_list, *member_ids_list),
    ).fetchall()
    for r in second_hop:
        member_ids.add(int(r["dst_file_id"]))
        pairwise_scores.append(float(r["s"]))
    return sorted(member_ids), pairwise_scores


def _hydrate_cluster(
    conn: sqlite3.Connection, seed_id: int, seed_path: str,
    member_ids: list[int], scores: list[float],
) -> ProjectCluster | None:
    rows = conn.execute(
        f"SELECT id, path FROM files "
        f"WHERE id IN ({','.join(['?'] * len(member_ids))})",
        member_ids,
    ).fetchall()
    paths = [r["path"] for r in rows]
    titles = []
    for r in rows:
        title_row = conn.execute(
            "SELECT text FROM chunks WHERE file_id = ? "
            "ORDER BY chunk_index ASC LIMIT 1",
            (int(r["id"]),),
        ).fetchone()
        title = _first_line_h1(title_row["text"] or "") if title_row else ""
        titles.append(title or r["path"])
    seed_title = next(
        (titles[i] for i, r in enumerate(rows) if int(r["id"]) == seed_id),
        seed_path,
    )
    avg_score = sum(scores) / len(scores) if scores else 0.0
    return ProjectCluster(
        seed_path=seed_path,
        seed_title=seed_title,
        member_paths=paths,
        member_titles=titles,
        score=avg_score,
        suggested_name=_suggest_project_name(titles),
    )


def _suggest_project_name(titles: list[str]) -> str:
    """Cheap heuristic: longest common word sequence in titles. If
    nothing useful jumps out, return the first title."""
    if not titles:
        return ""
    # Split each title into lowercase words; intersect.
    word_sets = [
        set(t.lower().split())
        for t in titles
    ]
    common = set.intersection(*word_sets) if word_sets else set()
    # Drop noise words.
    noise = {"the", "a", "an", "and", "or", "of", "to", "in", "on",
             "for", "with", "by", "at", "[meeting]", "[course]"}
    common -= noise
    if not common:
        # First title's first 5 words is a reasonable fallback.
        return " ".join(titles[0].split()[:5])
    # Pick the 3 most "characteristic" — longer words score higher.
    scored = sorted(common, key=lambda w: -len(w))
    return " ".join(scored[:3])


# ============================ Phase 75: I noticed X ===================

@dataclass
class Insight:
    key: str
    kind: str               # 'topic_spike' | 'health_drift'
    headline: str
    detail: str
    payload: dict = field(default_factory=dict)


def detect_topic_spikes(
    conn: sqlite3.Connection, *,
    min_docs: int = _TOPIC_SPIKE_MIN_DOCS,
) -> list[Insight]:
    """Find tokens (entities) appearing in unusually many recent docs.

    Compares the trailing 7d to the prior 21d. Tokens with N+ docs
    this week AND a > 2× density jump qualify as a spike.

    Uses the entities table (already populated by the spaCy NER hook)
    so this is essentially free.
    """
    now = time.time()
    week_cutoff = now - 7 * 86400
    prior_cutoff = now - 28 * 86400

    week_rows = conn.execute(
        "SELECT e.text_lower, e.label, COUNT(DISTINCT c.file_id) AS n_docs "
        "FROM entities e JOIN chunks c ON c.id = e.chunk_id "
        "JOIN files f ON f.id = c.file_id "
        "WHERE f.indexed_at >= ? "
        "  AND e.label IN ('PERSON', 'ORG', 'PRODUCT', 'WORK_OF_ART', 'EVENT', 'GPE') "
        "GROUP BY e.text_lower HAVING n_docs >= ? "
        "ORDER BY n_docs DESC",
        (week_cutoff, min_docs),
    ).fetchall()
    if not week_rows:
        return []
    insights: list[Insight] = []
    for r in week_rows:
        token = r["text_lower"]
        n_week = int(r["n_docs"])
        prior = conn.execute(
            "SELECT COUNT(DISTINCT c.file_id) AS n "
            "FROM entities e JOIN chunks c ON c.id = e.chunk_id "
            "JOIN files f ON f.id = c.file_id "
            "WHERE f.indexed_at >= ? AND f.indexed_at < ? "
            "  AND e.text_lower = ?",
            (prior_cutoff, week_cutoff, token),
        ).fetchone()
        n_prior = int(prior["n"] or 0) if prior else 0
        # Prior period is 3× the week, so density-normalise: prior
        # docs/3 should be the "expected" weekly count.
        expected = max(1, n_prior / 3.0)
        if n_week / expected < _TOPIC_SPIKE_PRIOR_RATIO:
            continue
        insights.append(Insight(
            key=f"topic_spike:{token}",
            kind="topic_spike",
            headline=f"You've referenced {r['text_lower']!r} in {n_week} docs this week",
            detail=(
                f"That's a {n_week / expected:.1f}× jump vs your prior "
                f"{n_prior} mentions in the previous 3 weeks. Want a synthesis?"
            ),
            payload={
                "token": token, "n_week": n_week, "n_prior": n_prior,
            },
        ))
    return insights[:10]


def detect_health_drift(
    conn: sqlite3.Connection,
    metric: str = "sleep_score",
    *,
    threshold_pct: float = 15.0,
) -> Insight | None:
    """When a metric's 7-day average drifts >= threshold% below the
    30-day average, flag it. Returns at most one insight per metric."""
    try:
        from . import health as health_mod
    except ImportError:
        return None
    week = health_mod.recent(conn, metric, days=7)
    month = health_mod.recent(conn, metric, days=30)
    if len(week) < 3 or len(month) < 10:
        return None  # not enough data
    week_avg = sum(p.value for p in week) / len(week)
    month_avg = sum(p.value for p in month) / len(month)
    if month_avg <= 0:
        return None
    delta_pct = (week_avg - month_avg) / month_avg * 100.0
    if delta_pct > -threshold_pct:
        return None
    return Insight(
        key=f"health_drift:{metric}",
        kind="health_drift",
        headline=(
            f"Your {metric.replace('_', ' ')} this week is "
            f"{abs(delta_pct):.0f}% below your 30-day average"
        ),
        detail=(
            f"7-day avg: {week_avg:.0f}; 30-day avg: {month_avg:.0f}. "
            f"Look at what changed in your schedule + meetings this week."
        ),
        payload={
            "metric": metric,
            "week_avg": week_avg,
            "month_avg": month_avg,
            "delta_pct": delta_pct,
        },
    )


def detect_insights(conn: sqlite3.Connection) -> list[Insight]:
    """All insight detectors stacked. Filters out insights already
    fired in the last 7 days (deduplicated by ``key``)."""
    _ensure_schema(conn)
    raw: list[Insight] = []
    raw.extend(detect_topic_spikes(conn))
    for metric in ("sleep_score", "readiness_score", "activity_score"):
        ins = detect_health_drift(conn, metric)
        if ins is not None:
            raw.append(ins)
    # Dedup against recent runs.
    cutoff = time.time() - _INSIGHT_DEDUP_DAYS * 86400
    fired_keys = {
        r["insight_key"]
        for r in conn.execute(
            "SELECT DISTINCT insight_key FROM synthesis_runs "
            "WHERE kind = 'insight' AND ran_at >= ?",
            (cutoff,),
        ).fetchall()
    }
    return [i for i in raw if i.key not in fired_keys]


def record_insight_fired(
    conn: sqlite3.Connection, insight: Insight,
) -> None:
    """Persist that we surfaced this insight so we don't re-fire
    within the dedup window."""
    _ensure_schema(conn)
    conn.execute(
        "INSERT INTO synthesis_runs(kind, ran_at, insight_key, payload_json) "
        "VALUES ('insight', ?, ?, ?)",
        (time.time(), insight.key, json.dumps(insight.payload)),
    )
    conn.commit()


# ============================ Phase 72: weekly review =================

@dataclass
class WeeklyReview:
    """The structured shape of a Sunday review. Renders to Markdown."""
    week_start: str   # 'YYYY-MM-DD' (Monday)
    week_end: str     # 'YYYY-MM-DD' (Sunday)
    n_docs_indexed: int
    n_meetings: int
    n_lectures: int
    n_jobs_applied: int
    tasks_done: list[str]
    tasks_lingering: list[str]      # open > 7d
    top_topics: list[tuple[str, int]]  # (entity, mention count)
    health_summary: str
    insights: list[Insight] = field(default_factory=list)


def assemble_weekly_review(
    conn: sqlite3.Connection,
) -> WeeklyReview:
    """Pull everything that happened this week (last 7 days) into one
    structured object. No LLM call required for the assembly — just
    data shaping."""
    now = time.time()
    week_cutoff = now - 7 * 86400
    from datetime import datetime as _dt
    end_dt = _dt.fromtimestamp(now)
    start_dt = _dt.fromtimestamp(week_cutoff)

    n_docs = conn.execute(
        "SELECT COUNT(*) AS n FROM files WHERE indexed_at >= ?",
        (week_cutoff,),
    ).fetchone()["n"] or 0
    # Round 27 fix (audit-found gap H6) — broaden the meetings /
    # lectures filters via the shared ``TRANSCRIPT_KIND_SQL``
    # constant. Round 24 migrated weekly_letter._signal_counts to
    # the constant; the synthesis weekly-review counts were missed,
    # so users with IMAP-delivered Granola/Otter transcripts saw
    # "0 meetings this week" even on busy weeks.
    n_meetings = conn.execute(
        f"SELECT COUNT(*) AS n FROM files f "
        f"WHERE f.indexed_at >= ? AND {TRANSCRIPT_KIND_SQL} "
        f"  AND EXISTS (SELECT 1 FROM chunks c WHERE c.file_id = f.id "
        f"              AND c.text LIKE '%[meeting]%')",
        (week_cutoff,),
    ).fetchone()["n"] or 0
    n_lectures = conn.execute(
        f"SELECT COUNT(*) AS n FROM files f "
        f"WHERE f.indexed_at >= ? AND {TRANSCRIPT_KIND_SQL} "
        f"  AND EXISTS (SELECT 1 FROM chunks c WHERE c.file_id = f.id "
        f"              AND c.chunk_index = 0 "
        f"              AND c.text LIKE '%[%')",
        (week_cutoff,),
    ).fetchone()["n"] or 0
    n_jobs = 0
    try:
        n_jobs = conn.execute(
            "SELECT COUNT(*) AS n FROM applications WHERE applied_at >= ?",
            (week_cutoff,),
        ).fetchone()["n"] or 0
    except sqlite3.OperationalError:
        pass

    # Tasks done this week.
    done_rows = conn.execute(
        "SELECT text FROM tasks WHERE status = 'done' "
        "AND completed_at >= ? "
        "ORDER BY completed_at DESC LIMIT 20",
        (week_cutoff,),
    ).fetchall()
    tasks_done = [r["text"] for r in done_rows]
    # Tasks lingering > 7 days.
    lingering_cutoff = now - 7 * 86400
    lingering_rows = conn.execute(
        "SELECT text FROM tasks WHERE status = 'open' "
        "AND created_at <= ? "
        "ORDER BY created_at ASC LIMIT 10",
        (lingering_cutoff,),
    ).fetchall()
    tasks_lingering = [r["text"] for r in lingering_rows]

    # Top entities (people / orgs / products) of the week.
    ent_rows = conn.execute(
        "SELECT e.text, COUNT(DISTINCT c.file_id) AS n "
        "FROM entities e JOIN chunks c ON c.id = e.chunk_id "
        "JOIN files f ON f.id = c.file_id "
        "WHERE f.indexed_at >= ? "
        "  AND e.label IN ('PERSON', 'ORG', 'PRODUCT', 'WORK_OF_ART', 'EVENT') "
        "GROUP BY e.text_lower ORDER BY n DESC LIMIT 8",
        (week_cutoff,),
    ).fetchall()
    top_topics = [(r["text"], int(r["n"])) for r in ent_rows]

    # Health summary line.
    health_summary = ""
    try:
        from . import health as health_mod
        bits = []
        for m, label in [
            ("sleep_score", "sleep"),
            ("readiness_score", "readiness"),
            ("activity_score", "activity"),
        ]:
            week_pts = health_mod.recent(conn, m, days=7)
            if not week_pts:
                continue
            avg = sum(p.value for p in week_pts) / len(week_pts)
            bits.append(f"{label} {avg:.0f}")
        if bits:
            health_summary = " · ".join(bits) + " (7d avg)"
    except ImportError:
        pass

    # Active insights.
    try:
        insights = detect_insights(conn)
    except Exception as e:  # noqa: BLE001
        log.warning("synthesis: insight detection failed: %s", e)
        insights = []

    return WeeklyReview(
        week_start=start_dt.strftime("%Y-%m-%d"),
        week_end=end_dt.strftime("%Y-%m-%d"),
        n_docs_indexed=int(n_docs),
        n_meetings=int(n_meetings),
        n_lectures=int(n_lectures),
        n_jobs_applied=int(n_jobs),
        tasks_done=tasks_done,
        tasks_lingering=tasks_lingering,
        top_topics=top_topics,
        health_summary=health_summary,
        insights=insights,
    )


def format_weekly_review_md(review: WeeklyReview) -> str:
    """Render a WeeklyReview as Markdown. Suitable for indexing as
    a doc, mailing, or piping."""
    lines = [
        f"# Weekly review — {review.week_start} → {review.week_end}",
        "",
        "## What happened",
        f"- Indexed {review.n_docs_indexed} new doc(s)",
    ]
    if review.n_meetings:
        lines.append(f"- {review.n_meetings} meeting(s) recorded")
    if review.n_lectures:
        lines.append(f"- {review.n_lectures} lecture(s) attended")
    if review.n_jobs_applied:
        lines.append(f"- {review.n_jobs_applied} job(s) applied to")
    if review.health_summary:
        lines.append(f"- Health: {review.health_summary}")

    if review.tasks_done:
        lines.extend(["", "## Done this week"])
        for t in review.tasks_done[:10]:
            lines.append(f"- ✓ {t}")
        if len(review.tasks_done) > 10:
            lines.append(f"- _... +{len(review.tasks_done) - 10} more_")

    if review.tasks_lingering:
        lines.extend(["", "## Lingering (open > 7d)"])
        for t in review.tasks_lingering:
            lines.append(f"- [ ] {t}")

    if review.top_topics:
        lines.extend(["", "## Top entities"])
        for topic, n in review.top_topics:
            lines.append(f"- **{topic}** — {n} doc(s)")

    if review.insights:
        lines.extend(["", "## Worth your attention"])
        for ins in review.insights:
            lines.append(f"- **{ins.headline}**")
            lines.append(f"  {ins.detail}")

    return "\n".join(lines).rstrip() + "\n"


def index_weekly_review(
    conn: sqlite3.Connection, embedder, cfg,
) -> str | None:
    """Generate + index this week's review as a doc. Returns the
    virtual_path on success.

    The review lands at ``review://YYYY-MM-DD`` (Sunday's date) so
    successive Sundays don't collide. Idempotent — re-indexing the
    same path overwrites."""
    _ensure_schema(conn)
    from .indexer import index_text

    review = assemble_weekly_review(conn)
    md = format_weekly_review_md(review)
    vp = f"review://{review.week_end}"
    try:
        index_text(
            conn, embedder, cfg,
            virtual_path=vp,
            title=f"Weekly review {review.week_start} → {review.week_end}",
            content=md,
            mtime=time.time(),
            kind="review",
            source="weekly_review",
        )
    except Exception as e:  # noqa: BLE001
        log.warning("synthesis: weekly review index failed: %s", e)
        return None
    conn.execute(
        "INSERT INTO synthesis_runs(kind, ran_at, virtual_path) "
        "VALUES ('weekly_review', ?, ?)",
        (time.time(), vp),
    )
    # Also record fired insights so they don't re-surface.
    for ins in review.insights:
        record_insight_fired(conn, ins)
    conn.commit()
    log.info("synthesis: weekly review indexed at %s", vp)
    return vp


def has_recent_weekly_review(
    conn: sqlite3.Connection, *, days: int = 6,
) -> bool:
    """Did we already generate a review in the last N days? Used by
    the daemon hook to avoid re-firing on a Sunday daemon restart."""
    _ensure_schema(conn)
    cutoff = time.time() - days * 86400
    row = conn.execute(
        "SELECT 1 FROM synthesis_runs "
        "WHERE kind = 'weekly_review' AND ran_at >= ? LIMIT 1",
        (cutoff,),
    ).fetchone()
    return row is not None


def run_weekly_review_if_due(
    cfg, conn: sqlite3.Connection, embedder,
) -> bool:
    """Daemon entrypoint. Sundays only, once per week."""
    from datetime import datetime as _dt
    if _dt.now().weekday() != 6:  # 6 = Sunday
        return False
    if has_recent_weekly_review(conn):
        return False
    return index_weekly_review(conn, embedder, cfg) is not None
